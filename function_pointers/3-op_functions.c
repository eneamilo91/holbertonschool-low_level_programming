#include <stdio.h>

/**
 *op_add - asdcsc
 *@a: ascsc
 *@b: ascsc
 *Return: acsac
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 *op_sub - ascsc
 *@a: ascac
 *@b: ascasc
 *Return: ascasc
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 *op_mul - ascsc
 *@a: ascs
 *@b: advdav
 *Return: adcadv
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 *op_div - ascc
 *@a: acadv
 *@b: advad
 *Return: adcs
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 *op_mod - ascascasc
 *@a: adcac
 *@b: acca
 *Return: adcdca
 */

int op_mod(int a, int b)
{
	return (a % b);
}
